package oxymat.demo;

public class Model {
    private String name;
    private int price;

    public Model(){

    }

    public String getName(){
        return name;
    }

    public void setName(String value){
        this.name = value;
    }

    public int getPrice(){
        return price;
    }

    public void setPrice(int value){
        this.price = value;
    }
}
